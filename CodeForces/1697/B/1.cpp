void solve() {
    ll N, Q;
    cin >> N >> Q;
    auto A = LIST(N);

    sort(ALL(A));
    Accumulate<ll> acc(A);
    rep(_, Q) {
        ll x, y;
        cin >> x >> y;
        ll res = acc.query(N-x, N-x+y);
        print(res);
    }
}


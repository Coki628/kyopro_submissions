void solve() {
    ll N;
    cin >> N;
    string S;
    cin >> S;

    ll cnt0 = count(ALL(S), '0');
    ll cnt1 = S.size() - cnt0;
    ll ans = cnt0 * cnt1;
    for (auto [k, v] : RLE(S)) {
        chmax(ans, (ll)v*v);
    }
    print(ans);
}


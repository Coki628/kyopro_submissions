void solve() {
    ll N;
    cin >> N;
    auto A = LIST(N);

    ll cnt0 = count(ALL(A), 0);
    if (cnt0) {
        ll ans = N - cnt0;
        print(ans);
        return;
    }
    UNIQUE(A);
    ll ans = A.size() < N ? N : N+1;
    print(ans);
}


void solve() {
    ll N;
    cin >> N;
    string S;
    cin >> S;

    auto [mxcnt, _] = most_common(Counter(S))[0];
    ll others = N - mxcnt;
    ll rest = max(mxcnt - others, 0LL);
    if (rest == 0 and N % 2 == 1) {
        rest++;
    }
    print(rest);
}


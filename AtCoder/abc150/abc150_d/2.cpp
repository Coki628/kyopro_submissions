void solve() {
    ll N, M;
    cin >> N >> M;
    auto A = LIST(N);

    // まず全LCMを取ってMで収まるか(後で/2するからM*2でチェック)
    ll l = 1;
    for (ll a : A) {
        l = lcm(l, a);
        if (l > M*2) {
            print(0);
            return;
        }
    }
    // 中国剰余定理で全部が交わる場所を見つける
    vector<ll> R(N), mo(N);
    rep(i, N) {
        // (a/2) mod a の形を全部集める
        R[i] = A[i]/2;
        mo[i] = A[i];
    }
    auto res = crt(R, mo);
    // 解なし or あるけど最初からM超 ならNG
    if (res.first == 0 and res.second == 0 or res.first > M) {
        print(0);
        return;
    }
    ll m = res.second;
    ll base = res.first;
    // 周期何回行けるか
    // ll ret = bisearch_max(0, INF, [&](ll x) {
    //     if (base+m*(ld)x > INF) return false; 
    //     return base+m*x <= M;
    // });
    ll ret = (M-base)/m;    
    // baseの分足す
    ret++;
    print(ret);
}


void solve() {
    ll N, K;
    cin >> N >> K;
    auto A = LIST(N);
    auto B = LIST(K);

    vector<ll> C(N);
    rep(i, K) {
        B[i]--;
        C[B[i]] = 1;
    }
    ll mx = max(A);
    rep(i, N) {
        if (A[i] == mx and C[i]) {
            Yes();
            return;
        }
    }
    No();
}


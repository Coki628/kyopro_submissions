void solve() {
    ll N;
    cin >> N;
    vector<ll> A(N);
    cin >> A;

    ll mn = 0, cur = 0;
    for (auto a : A) {
        cur += a;
        chmin(mn, cur);
    }
    ll init = mn < 0 ? -mn : 0;
    cur = init;
    for (auto a : A) {
        cur += a;
    }
    print(cur);
}


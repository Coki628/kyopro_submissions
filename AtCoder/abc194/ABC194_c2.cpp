/**
 * 参考：https://twitter.com/kichi2004_/status/1368205398256226307
 * ・別解、シグマの式変形
 * ・制約が緩くなくても解ける式変形の解法。
 * 　公式解説のはちょっと分からなくて、kichiさんが分かりやすいのを出してくれていたので、
 * 　こっちでも解く。噛み砕いた説明は自分ノートにも書いたけど、ここにもざっと書いておく。
 * 　イメージが定着するように、プログラムの処理と結びつけて考える。
 * ・今回の場合、まずは展開。これは大丈夫だと思う。(a-b)^2 = a^2 -2ab +b^2 ってやつね。
 * ・で、項が分けれたら、シグマも各項毎に分ける。これはプログラムでは、
 * 　各項毎で内側ループを3回に分けて回しても答えが変わらないことに該当する。
 * ・次に、シグマに関係ない項を外に出す。これでその部分のシグマ記号が消せる。
 * 　内ループの添字に関係ない処理を外側に出して、内ループの回数分掛け算することに該当する。
 * ・最後に、シグマに関係ない係数を外に出す。これでシグマの中が
 * 　そのシグマに関係のあることだけに絞り込める。
 * 　内ループの添字に関係ない掛け算を外側に出して、その内ループが終わったところで
 * 　まとめて掛け算することに該当する。
 * ・以上を全てやると、内ループでやる処理はA[j]の区間和とA[j]^2の区間和のみになるので、
 * 　これは累積和を取って前計算できる。
 */


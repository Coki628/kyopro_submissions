void solve() {
    ll N;
    cin >> N;

    BIT<ll> bit(N);
    rep(i, N) {
        ll x, y;
        cin >> x >> y;
        ll sm = bit.query(i-x, i);
        if (sm >= y) {
            bit.add(i, 1);
        }
    }
    ll ans = bit.query(0, N);
    print(ans);
}


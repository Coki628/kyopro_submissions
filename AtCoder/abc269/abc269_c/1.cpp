void solve() {
    ll S;
    cin >> S;

    vector<ll> ans = {S};
    ll T = S;
    while (T > 0) {
        T = (T-1) & S;
        ans.eb(T);
    }
    sort(ALL(ans));
    print(ans, '\n');
}


void solve() {
    ll a, b;
    cin >> a >> b;

    ll ans = 0;
    while (a != b) {
        if (a < b) swap(a, b);
        auto [d, m] = divmod(a, b);
        ans += d;
        if (m == 0) {
            ans--;
            break;
        }
        a = m;
    }
    print(ans);
}


void solve() {
    ll N, M;
    cin >> N >> M;
    vvi nodes(N);
    rep(i, M) {
        ll u, v;
        cin >> u >> v;
        u--; v--;
        nodes[u].eb(v);
        nodes[v].eb(u);
    }

    auto dist = bfs(nodes, {0});
    rep(i, N) {
        if (dist[i] >= INF) dist[i] = -1;
        print(dist[i]);
    }
}

